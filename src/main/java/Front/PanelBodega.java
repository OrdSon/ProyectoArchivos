    private Inventario recopilarObjeto(boolean negativo) {
        try {
            int id = Integer.parseInt(idTxt.getText());
            String nombre = nombreTxt.getText();
            String marca = marcaTxt.getText();
            cantidadSpinner.commitEdit();
            int cant = (Integer) cantidadSpinner.getValue();
            if (negativo) {
                cant = (cant * -1);
            }
            return new Inventario(id, nombre, marca, empleado.sucursal(),nombre , cant);
        } catch (NumberFormatException | ParseException e) {
            return null;
        }
    }

    private void getProductRowData() {
        DefaultTableModel model = (DefaultTableModel) productosTabla.getModel();
        int selectedRowIndex = productosTabla.getSelectedRow();
        idTxt.setText(model.getValueAt(selectedRowIndex, 0).toString());
        marcaTxt.setText(model.getValueAt(selectedRowIndex, 1).toString());
        nombreTxt.setText(model.getValueAt(selectedRowIndex, 3).toString());

    }

    private void getInventoryRowData() {
        DefaultTableModel model = (DefaultTableModel) inventarioTabla.getModel();
        int selectedRowIndex = inventarioTabla.getSelectedRow();
        idTxt.setText(model.getValueAt(selectedRowIndex, 0).toString());
        marcaTxt.setText(model.getValueAt(selectedRowIndex, 2).toString());
        nombreTxt.setText(model.getValueAt(selectedRowIndex, 1).toString());
    }

