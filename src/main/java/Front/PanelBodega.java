    private void insertar(boolean negativo) {
        if (idTxt.getText().isBlank() && nombreTxt.getText().isBlank() && marcaTxt.getText().isBlank()) {
            JOptionPane.showMessageDialog(null, "Alguno de los campos debe contener información \nPuede seleccionar un producto haciendo click sobre el");
            return;
        }
        if (idTxt.getText().isBlank()) {
            JOptionPane.showMessageDialog(null, "Necesita seleccionar un producto\nPuede seleccionar un producto haciendo click sobre el");
            return;
        }
        inventarioDAO.insert(recopilarObjeto(negativo));
    }
